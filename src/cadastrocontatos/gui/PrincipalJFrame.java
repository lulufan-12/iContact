    private void btnOutroConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOutroConsultarActionPerformed
        int index = tableOutrosContatos.getSelectedRow();
        if(index >= 0){
            Contato contato = modeloTabelaOutros.getContato(index);
            OutroJDialog.executar(null, OperacaoCadastro.ocConsultar, contato);
        }
    }//GEN-LAST:event_btnOutroConsultarActionPerformed

    private void btnOutroAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOutroAdicionarActionPerformed
        Contato contato = new Contato();
        boolean confirmado = OutroJDialog.executar(this, OperacaoCadastro.ocIncluir, contato);
        if(confirmado){
            modeloTabelaOutros.insertContato(contato);
        }
    }//GEN-LAST:event_btnOutroAdicionarActionPerformed

    private void btnOutroAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOutroAlterarActionPerformed
        int index = tableOutrosContatos.getSelectedRow();
        if(index >= 0){
            Contato contato = modeloTabelaOutros.getContato(index);
            boolean confirmado = OutroJDialog.executar(this, OperacaoCadastro.ocAlterar, contato);
            if(confirmado){
                modeloTabelaOutros.setContato(index, contato);
            }
        }
    }//GEN-LAST:event_btnOutroAlterarActionPerformed

    private void btnOutroRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOutroRemoverActionPerformed
        int index = tableOutrosContatos.getSelectedRow();
        if(index >= 0){
            int resp = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir esse contato?", "Excluir contato", JOptionPane.YES_NO_OPTION);
            if(resp == JOptionPane.NO_OPTION) return;
            modeloTabelaOutros.deleteContato(index);
        }
    }//GEN-LAST:event_btnOutroRemoverActionPerformed

